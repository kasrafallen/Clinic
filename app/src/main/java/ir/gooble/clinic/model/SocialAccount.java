package ir.gooble.clinic.model;

public class SocialAccount {

    private String AccountName;
    private String SoccialName;
    private String SocialLink;
    private String SocialLogo;

    public String getAccountName() {
        return AccountName;
    }

    public void setAccountName(String accountName) {
        AccountName = accountName;
    }

    public String getSoccialName() {
        return SoccialName;
    }

    public void setSoccialName(String soccialName) {
        SoccialName = soccialName;
    }

    public String getSocialLink() {
        return SocialLink;
    }

    public void setSocialLink(String socialLink) {
        SocialLink = socialLink;
    }

    public String getSocialLogo() {
        return SocialLogo;
    }

    public void setSocialLogo(String socialLogo) {
        SocialLogo = socialLogo;
    }
}
